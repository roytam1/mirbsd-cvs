wint_t
towlower(wint_t x)
{
	if (!__locale_is_utf8)
		return ((x < 0x100) ? tolower(x) : x);
	else
		return ((x < 0xFFFD) ?
		    (x + tolower_table[x >> 8][x & 0xff]) : x);
}

