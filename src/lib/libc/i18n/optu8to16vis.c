size_t
optu8to16vis(wchar_t * restrict pwc, const char * restrict src, size_t n,
    mbstate_t * restrict ps)
{
	static mbstate_t internal_mbstate = { 0, 0 };
	size_t rv;

	if (__predict_false(ps == NULL))
		ps = &internal_mbstate;

	rv = optu8to16(pwc, src, n, ps);
	if (pwc && iswoctet(*pwc))
		*pwc = vistable[*pwc & 0x7F];
	return (rv);
}
